function calculateLuminance(r: number, g: number, b: number): number {
  r = r / 255;
  g = g / 255;
  b = b / 255;

  return (
    0.2125 * (r <= 0.03928 ? r / 12.92 : ((r + 0.055) / 1.055) ** 2.4) +
    0.7152 * (g <= 0.03928 ? r / 12.92 : ((g + 0.055) / 1.055) ** 2.4) +
    0.0722 * (b <= 0.03928 ? r / 12.92 : ((b + 0.055) / 1.055) ** 2.4)
  );
}
